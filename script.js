var startTime = new Date().getTime();
function getRandomColor() {

  if (Math.random() < 0.2) {
    return "blue";
 
  }else if (Math.random() < 0.4) {
    return "yellow";

  }else if (Math.random() < 0.6) {
    return "green";

  }else if (Math.random() < 0.8) {
    return "red";

  }else {
    return "orange";
  }
}
function makeShapeAppear() {
  var top = Math.random() * 400;
  var left = Math.random() * 400;
  var size = Math.random() * 200 + 50;
  var color = getRandomColor();

if (Math.random() > 0.5) {
  document.getElementById("shape").style.borderRadius = "50%";
} else {
  document.getElementById("shape").style.borderRadius = "0%";
}
  document.getElementById("shape").style.backgroundColor = color;

  document.getElementById("shape").style.top = top + "px";
  document.getElementById("shape").style.left = left + "px";
  document.getElementById("shape").style.width = size + "px";
  document.getElementById("shape").style.height = size + "px";

document.getElementById("shape").style.display = "block";

startTime = new Date().getTime();
}

function appearAfterDelay(){

setTimeout(makeShapeAppear, Math.random() * 2000);

}

appearAfterDelay();

document.getElementById("shape").onclick = function(){
  document.getElementById("shape").style.display = "none";
  var endTime = new Date().getTime();
  var reactionTime = (endTime - startTime) / 1000;
  document.getElementById("timeTaken").innerHTML = reactionTime + "s";
  appearAfterDelay();
}
